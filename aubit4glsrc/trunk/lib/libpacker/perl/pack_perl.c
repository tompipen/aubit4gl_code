/**
 *
 * @todo Describe function
 */
int
A4GLPacker_A4GL_open_packer (char *basename, char dir,char *packname,char *version)
{
  char buff[256];
  sprintf (buff, "%s.pl", basename);


  if (toupper (dir) == 'O')
    {
      outfile = fopen (buff, "w");
      if (outfile)
	{
	  fprintf (outfile, "#!/bin/perl\n");
	  fprintf (outfile, "%%report=(\n");	/* warning: unknown conversion type character `r' in format */

	  return 1;
	}
      return 0;
    }

  if (toupper (dir) == 'I')
    {
      out_only ();
      return 0;
    }

  return 0;

}

/**
 *
 * @todo Describe function
 */
void
A4GLPacker_A4GL_close_packer (char dir)
{
  if (toupper (dir) == 'O')
    {
      fprintf (outfile, ");\n");

      fclose (outfile);
    }

  if (toupper (dir) == 'I')
    out_only ();
}


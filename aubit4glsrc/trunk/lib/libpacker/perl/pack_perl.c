/**
 *
 * @todo Describe function
 */
char *
A4GL_escape_str (char *s, char q, char e)
{
  static char buff[2000];
  int buffcnt = 0;
  int a;
  for (a = 0; a < strlen (s); a++)
    {
      if (s[a] == e)
	{			/* Escape our escape character */
	  buff[buffcnt++] = e;
	  buff[buffcnt++] = e;
	  continue;
	}
      if (s[a] == q)
	{			/* Escape a quote */
	  buff[buffcnt++] = e;
	  buff[buffcnt++] = q;
	  continue;
	}

      buff[buffcnt++] = s[a];
    }

  buff[buffcnt++] = 0;

  return buff;
}


